class Math:
    def __init__(self, arg_1, arg_2):
        self.arg_1 = arg_1
        self.arg_2 = arg_2

    def addition(self):
        return self.arg_1 + self.arg_2

    def subtraction(self):
        return self.arg_1 - self.arg_2

    def multiplication(self):
        return self.arg_1 * self.arg_2

    def division(self):
        return self.arg_1 / self.arg_2
